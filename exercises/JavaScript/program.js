var body;

// implement onload function
window.onload = function() {
    body = document.querySelector("body");
}

// implement changeColorBlue
window.changeColorBlue = function() {
    body.style.backgroundColor = "#a8ceff";
}

// implement changeColorPurple
window.changeColorGreen = function() {
    body.style.backgroundColor = "#148F77";
}
